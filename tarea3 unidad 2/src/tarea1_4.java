public class tarea1_4
{
    public static void main(String[] args) {
        /*diferencia entre float y double*/


        float pi_f = 3.14159265359F; //float no aguanta mas de 7 decimales y de ahi lo redondea
        double pi_d = 3.14159265359; //double aguanta hasta 16 dígitos decimales

        System.out.println("pi en float " + pi_f);
        System.out.println("pi en double " + pi_d);

    }
}
